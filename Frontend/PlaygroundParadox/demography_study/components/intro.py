def render_intro():
    st.title("Генетическая экспансия")
    st.markdown("""
**Исследование критической плотности родственных связей в закрытых группах.**

Этот эксперимент изучает «эффект невидимых нитей». Мы привыкли воспринимать группу из $N$ человек как набор случайных единиц, но если в обществе есть многодетные семьи, группа мгновенно превращается в **сеть**.

---

### 🔍 Предмет исследования
Мы анализируем, как быстро случайная выборка людей «захватывается» представителями одних и тех же семей. В демографии это объясняет, почему мир кажется «тесным»: нам достаточно встретить одного ребенка из семьи из пяти детей, чтобы статистически «связаться» со всеми его братьями и сестрами.

### 🧬 Механика симуляции
1.  **Формирование среды:** Мы создаем город из тысяч семей с заданным распределением (от одиночек до многодетных кланов).
2.  **Случайный отбор:** Система начинает «выдергивать» из города по одному ребенку, формируя группу размером $N$.
3.  **Детекция связей:** После каждого нового участника алгоритм проверяет: *есть ли у него в этой группе брат или сестра?*
4.  **Фиксация взрыва:** Мы ищем точку, где вероятность встретить родственника перестает быть математической погрешностью и становится неизбежностью.

---

### 📊 Что покажет график:
*   **Ось X:** Размер группы ($N$) — от 2 до 100 человек.
*   **Ось Y:** Вероятность встречи хотя бы одной пары родственников (0.0 — 1.0).
*   **Индикатор «Точки перелома»:** Тот самый размер группы, при котором шанс родства превышает 50%.

---

### 🚀 Запустить сканирование?
Введите **название варианта** или укажите **свое распределение** (например: *«1 реб — 50%, 2 реб — 30%, 3 реб — 20%»*). 

Институт готов к построению визуализации.
""")
